const calculator = {
  add: function(a, b) {
    console.log(a + b);
  },
  
  minus: function(a, b) {
    console.log(a - b);
  },

  divide: function(a, b) {
    console.log(a / b);
  },

  powerof: function(a, b) {
    console.log(a ** b);
  }
}

calculator.add(20, 30);
calculator.minus(30, 20);
calculator.divide(9, 3);
calculator.powerof(2, 2);





